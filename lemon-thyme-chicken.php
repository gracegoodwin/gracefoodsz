                    <ol>
                        <li>Preheat the oven to 425 degrees. Whisk olive oil, thyme, garlic, the zest and juice of one lemon, salt, and pepper in a large bowl.</li>
                        <li>Cut the remaining lemon into wedges. Add the chicken and lemon wedges to the bowl. Toss well.</li>
                        <li>Dump the mixture into a rimmed baking sheet (or cast iron pan). Make sure chicken is skin side up and spaced apart evenly.</li>
                        <li>Roast on the middle rack, rotating the pan halfway through until chicken is golden brown and registers at 165 degrees; about 50 minutes. Remove to a platter and sprinkle with parsley. Drizzle with juices from the pan and squeeze roasted lemon wedges on the chicken.</li>
