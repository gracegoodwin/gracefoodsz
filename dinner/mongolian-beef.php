                    <ol>
                        <li>Heat oil in a saucepan over medium-low heat. Add ginger and garlic and saute. Combine soy sauce, hoisin sauce, water, and brown sugar in a bowl. Pour mixture into the saucepan and boil until slightly thick. Set aside.</li>
                        <li>While sauce cooks, cut flank steak into 1/4 inch slices and cover in corn starch.</li>
                        <li>Add oil to a skillet and heat over medium-high heat. Working in batches, cook meat until done, about 2 minutes.</li>
                        <li>Once cooked, combine with the sauce over low heat. Serve over udon noodles or rice.</li>
