            <ol>
                <li>In 1/2 cup of the warm water, pour in the sugar and yeast. Do not stir. Let sit for 5 minutes, and then stir the yeast and sugar mixture until it all dissolves in the water.</li>
                <li>Mix the flour and salt in a large bowl. Make a well in the middle and pour in the yeast and sugar mixture.</li>
                <li>Pour 1/3 cup of warm water into the well. Mix and stir in the rest of the water as needed. Depending on where you live, you may need to add an additional couple tablespoons to about 1/4 cup of water. You want a moist and firm dough after you have mixed it.</li>
                <li>On a floured countertop, knead the dough for about 10 minutes until it is smooth and elastic. Try working in as much flour as possible to form a firm and stiff dough.</li>
                <li>Lightly brush a large bowl with oil and turn the dough to coat. Cover the bowl with a damp dish towel. Let rise in a warm place for 1 hour, until the dough has doubled in size. Punch the dough down, and let it rest for another 10 minutes.</li>
                <li>Carefully divide the dough into 8 pieces. Shape each piece into a round. Now, take a dough ball, and press it gently against the countertop moving your hand and the ball in a circular motion pulling the dough into itself while reducing the pressure on top of the dough slightly until a perfect dough ball forms. Repeat with 7 other dough rounds.</li>
                <li>Coat a finger in flour, and gently press your finger into the center of each dough ball to form a ring. Stretch the ring to about 1/3 the diameter of the bagel and place on a lightly oiled cookie sheet. Repeat the same step with the remaining dough.</li>
                <li>After shaping the bagels and placing them on the cookie sheet, cover with a damp kitchen towel and allow to rest for 10 minutes. Meanwhile, preheat your oven to 425 degrees.</li>
                <li>Bring a large pot of water to a boil. Reduce the heat. Use a slotted spoon or skimmer to lower the bagels into the water. Boil as many as you are comfortable with boiling. Once the bagels are in, it shouldnâ€™t take too long for them to float to the top (a couple seconds). Let them sit there for 1 minute, and then flip them over to boil for another minute.</li>
                <li>If you want to add toppings to your bagels, do so as you take them out of the water. Alternatively, you can use an egg wash to get the toppings to stick before baking the bagels.</li>
                <li>Once all the bagels have boiled (and have been topped with your choice of toppings), transfer them to an oiled or parchment-lined baking sheet.</li>
                <li>Bake for 20 - 25 minutes, or until golden brown.</li>
                <li>Cool on a wire rack.</li>
