                    <p class="recipe-page-title">Instructions:</p>
                    <ol>
                        <li>Preheat oven to 350 degrees and butter a 9"x5" loaf plan. In a large bowl, whisk together flour, pumpkin pie spice baking soda, baking powder, and salt.</li>
                        <li>In another large bowl, combine pumpkin, 1 cup of sugar, butter, sour cream, 1 egg and yolk, and vanilla until smooth. Pour wet over dry and fold until combined.</li>
                        <li>Make cream cheese filling: in a small bowl, stir together cream cheese, remaining egg, 2 tablespoons of flour, and 3 tablespoons of sugar.</li>
                        <li>Transfer half the pumpkin batter in the pan. Dollop cream cheese filling on top and smooth in an even layer. Top with remianinf pumpkin butter.</li>
                        <li>Bake until deeply golden and a toothpick comes out clean, about 1 hour and 5 minutes.</li>
                        <li>Let cool for at least 10 minutes before serving.</li>
